def ex1range():
    a_range = range(5, 15)
    print(a_range)

    list_a_range = list(a_range)
    print(list_a_range)

    tuple_a_range = tuple(a_range)
    print(tuple_a_range)

def ex2range():
    a_range = range(1, 10, 2)

    print(a_range)

    list_a_range = list(a_range)

    print(list_a_range)

    tuple_a_range = tuple(a_range)

    print(tuple_a_range)